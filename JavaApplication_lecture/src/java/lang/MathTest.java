package java.lang;

class MathTest 
{  
	public static void main(String args[]) 
	{
		System.out.println("파이 :  " + Math.PI);
		System.out.println("10 절대값 :  " + Math.abs(10));
		System.out.println("큰 값을 반환합니다. : "  + Math.max(30, 60));
		System.out.println("작은 값을 반환합니다. : " + Math.min(10.0, 20.0)); 
	}
}
